int		ft_is_prime(int nb);

int		ft_is_prime(int nb)
{
	int		n;

	n = 2;
	if (nb < 2)
		return (0);
	while ((nb % n) != 0)
		n++;
	if (n == nb)
		return (1);
	return (0);
}
